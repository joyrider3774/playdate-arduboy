/*
 * マップを取得する
 */
uint8_t FieldGetMap(uint8_t positionX, uint8_t positionY)
{
  return pgm_read_byte(fieldMaps + (positionY & 0xf0) + (positionX >> 4));
}

/*
 * 距離から高さを取得する
 */
uint8_t FieldGetDistanceHeight(uint8_t distance)
{
  uint8_t height = 0x00;
  if (distance < FIELD_DISTANCE_FAR) {
    height = pgm_read_byte(fieldDistanceHeights + distance);
  }
  return height;
}

/*
 * ビューの距離を取得する
 */
uint8_t FieldGetViewDistance(int16_t x)
{
  uint8_t distance = 0xff;
  if (FIELD_VIEW_LEFT <= x && x <= FIELD_VIEW_RIGHT) {
    distance = fieldViewDistances[(x - FIELD_VIEW_LEFT) >> 1];
  }
  return distance;
}

/*
 * ビューのマップを取得する
 */
uint8_t FieldGetViewMap(int16_t x)
{
  uint8_t map = 0x00;
  if (FIELD_VIEW_LEFT <= x && x <= FIELD_VIEW_RIGHT) {
    map = fieldViewMaps[(x - FIELD_VIEW_LEFT) >> 1];
  }
  return map;
}

/*
 * ビューの距離とマップを設定する
 */
void FieldSetViewDistanceAndMap(int16_t x, uint8_t distance, uint8_t map)
{
  if (FIELD_VIEW_LEFT <= x && x <= FIELD_VIEW_RIGHT) {
    int8_t i = (x - FIELD_VIEW_LEFT) >> 1;
    fieldViewDistances[i] = distance;
    fieldViewMaps[i] = map;
  }
}

/*
 * フィールドのビューを計算する
 */
static void FieldCalcView(void)
{
  uint8_t raycastX, raycastY;
  uint8_t raycastMap;
  int16_t raycastDistance;
  uint8_t angle = fieldCameraAngle - (FIELD_VIEW >> 1);
  for (int8_t i = 0x00; i < FIELD_VIEW; i++) {
    FieldRaycast(fieldCameraPositionX, fieldCameraPositionY, angle, &raycastX, &raycastY, &raycastDistance, &fieldViewMaps[i], &fieldViewSlices[i]);
    if (raycastDistance < FIELD_DISTANCE_FAR) {
      fieldViewDistances[i] = (uint8_t)((raycastDistance * pgm_read_byte(fieldCosViews + i)) >> 8);
    } else {
      fieldViewDistances[i] = 0xff;
    }
    ++angle;
  }
}

/*
 * フィールドのビューを描画する
 */
static void FieldDrawView(void)
{
  uint8_t *lower = SystemGetVideoBuffer() + WIDTH * ((HEIGHT / 0x08) / 0x02) + FIELD_VIEW_LEFT;
  uint8_t *upper = lower - WIDTH;
  for (int8_t i = 0x00; i < FIELD_VIEW; i++) {
    uint8_t distance = fieldViewDistances[i];
    if (distance < FIELD_DISTANCE_FAR) {
      uint8_t *pattern = patternTable + 0x0300 + (fieldViewMaps[i] << 4) + fieldViewSlices[i];
      uint8_t srcUpper = pgm_read_byte(pattern + 0x0000);
      uint8_t srcLower = pgm_read_byte(pattern + 0x0080);
      uint32_t dstUpper = 0x00000000;
      uint32_t dstLower = 0x00000000;
      uint8_t *scaleY = fieldScaleYs + (distance << 3);
      uint8_t shift = 0x18;
      for (uint8_t y = 0x00; y < 0x08; y++) {
        uint8_t su = srcUpper & 0x80;
        uint32_t sl = (uint32_t)(srcLower & 0x01) << 31;
        uint8_t sy = pgm_read_byte(scaleY);
        shift -= sy;
        while (sy > 0x00) {
          dstUpper = (dstUpper | su) << 1;
          dstLower = (dstLower >> 1) | sl;
          --sy;
        }
        srcUpper <<= 1;
        srcLower >>= 1;
        ++scaleY;
      }
      if (shift > 0x00) {
        dstUpper <<= shift;
        dstLower >>= shift;
      }
      *(upper - 0x0000) = (uint8_t)(dstUpper >> 24);
      *(upper - 0x0001) = (uint8_t)(dstUpper >> 24);
      *(upper - 0x0080) = (uint8_t)(dstUpper >> 16);
      *(upper - 0x0081) = (uint8_t)(dstUpper >> 16);
      *(upper - 0x0100) = (uint8_t)(dstUpper >> 8);
      *(upper - 0x0101) = (uint8_t)(dstUpper >> 8);
      *(lower + 0x0000) = (uint8_t)(dstLower >> 8);
      *(lower + 0x0001) = (uint8_t)(dstLower >> 8);
      *(lower + 0x0080) = (uint8_t)(dstLower >> 16);
      *(lower + 0x0081) = (uint8_t)(dstLower >> 16);
      *(lower + 0x0100) = (uint8_t)(dstLower >> 24);
      *(lower + 0x0101) = (uint8_t)(dstLower >> 24);
    }
    upper += 0x02;
    lower += 0x02;
  }
}

/*
 * レイキャストの計算を行う
 */
static void FieldRaycast(uint8_t positionX, uint8_t positionY, uint8_t angle, uint8_t *raycastX, uint8_t *raycastY, uint16_t *raycastDistance, uint8_t *raycastMap, uint8_t *raycastSlice)
{
  // 垂直位置の初期化
  uint8_t vx, vy;
  uint8_t vm = 0x00;
  uint16_t vd = 0x7fff;

  // 水平位置の初期化
  uint8_t hx, hy;
  uint8_t hm = 0x00;
  uint16_t hd = 0x7fff;
    
  // 000°
  if (angle == 0x00) {
    uint8_t i = (positionY & 0xf0) + ((positionX >> 4) + 0x01);
    uint8_t m = 0x00;
    while ((m = pgm_read_byte(fieldMaps + i)) == 0x00) {
      i += 0x01;
    }
    vx = (i & 0x0f) << 4;
    vy = positionY;
    vd = (int16_t)(vx - positionX);
    vm = m;

  // 000°〜090°
  } else if (angle < 0x40) {
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + angle);
      uint8_t x = (positionX >> 4) + 0x01;
      int32_t y = ((int32_t)positionY << 8) + (int32_t)(0x10 - (positionX & 0x0f)) * t;
      t *= 0x00000010;
      while (y < 0x00010000) {
        if ((vm = pgm_read_byte(fieldMaps + ((uint8_t)(y >> 8) & 0xf0) + x)) != 0x00) {
          break;
        }
        x += 0x01;
        y += t;
      }
      if (vm != 0x00) {
        vx = x << 4;
        vy = (uint8_t)(y >> 8);
        vd = ((uint16_t)(vx - positionX) << 8) / pgm_read_byte(fieldCosDistances + angle);
      }
    }
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + 0x40 - angle);
      int32_t x = ((int32_t)positionX << 8) + (int32_t)(0x10 - (positionY & 0x0f)) * t;
      uint8_t y = (positionY & 0xf0) + 0x10;
      t *= 0x00000010;
      while (x < 0x00010000) {
        if ((hm = pgm_read_byte(fieldMaps + y + ((uint8_t)(x >> 12) & 0x0f))) != 0x00) {
          break;
        }
        x += t;
        y += 0x10;
      }
      if (hm != 0x00) {
        hx = (uint8_t)(x >> 8);
        hy = y;
        hd = ((uint16_t)(hy - positionY) << 8) / pgm_read_byte(fieldCosDistances + 0x40 - angle);
      }
    }
    
  // 090°
  } else if (angle == 0x40) {
    uint8_t i = ((positionY & 0xf0) + 0x10) + (positionX >> 4);
    uint8_t m = 0x00;
    while ((m = pgm_read_byte(fieldMaps + i)) == 0x00) {
      i += 0x10;
    }
    hx = positionX;
    hy = i & 0xf0;
    hd = hy - positionY;
    hm = m;

  // 090°〜180°
  } else if (angle < 0x80) {
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + 0x80 - angle);
      uint8_t x = (positionX >> 4) - 0x01;
      int32_t y = ((int32_t)positionY << 8) + (int32_t)(0x01 + (positionX & 0x0f)) * t;
      t *= 0x00000010;
      while (y < 0x00010000) {
        if ((vm = pgm_read_byte(fieldMaps + ((uint8_t)(y >> 8) & 0xf0) + x)) != 0x00) {
          break;
        }
        x -= 0x01;
        y += t;
      }
      if (vm != 0x00) {
        vx = (x << 4) + 0x0f;
        vy = (uint8_t)(y >> 8);
        vd = ((uint16_t)(positionX - vx) << 8) / pgm_read_byte(fieldCosDistances + 0x80 - angle);
      }
    }
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + angle - 0x40);
      int32_t x = ((int32_t)positionX << 8) - (int32_t)(0x10 - (positionY & 0x0f)) * t;
      uint8_t y = (positionY & 0xf0) + 0x10;
      t *= 0x00000010;
      while (x >= 0x00000000) {
        if ((hm = pgm_read_byte(fieldMaps + y + ((uint8_t)(x >> 12) & 0x0f))) != 0x00) {
          break;
        }
        x -= t;
        y += 0x10;
      }
      if (hm != 0x00) {
        hx = (uint8_t)(x >> 8);
        hy = y;
        hd = ((uint16_t)(hy - positionY) << 8) / pgm_read_byte(fieldCosDistances + angle - 0x40);
      }
    }
    
  // 180°
  } else if (angle == 0x80) {
    uint8_t i = (positionY & 0xf0) + ((positionX >> 4) - 0x01);
    uint8_t m = 0x00;
    while ((m = pgm_read_byte(fieldMaps + i)) == 0x00) {
      i -= 0x01;
    }
    vx = ((i & 0x0f) << 4) + 0x0f;
    vy = positionY;
    vd = positionX - vx;
    vm = m;

  // 180°〜270°
  } else if (angle < 0xc0) {
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + angle - 0x80);
      uint8_t x = (positionX >> 4) - 0x01;
      int32_t y = ((int32_t)positionY << 8) - (int32_t)(0x01 + (positionX & 0x0f)) * t;
      t *= 0x00000010;
      while (y >= 0x00000000) {
        if ((vm = pgm_read_byte(fieldMaps + ((uint8_t)(y >> 8) & 0xf0) + x)) != 0x00) {
          break;
        }
        x -= 0x01;
        y -= t;
      }
      if (vm != 0x00) {
        vx = (x << 4) + 0x0f;
        vy = (uint8_t)(y >> 8);
        vd = ((uint16_t)(positionX - vx) << 8) / pgm_read_byte(fieldCosDistances + angle - 0x80);
      }
    }
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + 0xc0 - angle);
      int32_t x = ((int32_t)positionX << 8) - (int32_t)(0x01 + (positionY & 0x0f)) * t;
      uint8_t y = (positionY & 0xf0) - 0x10;
      t *= 0x00000010;
      while (x >= 0x00000000) {
        if ((hm = pgm_read_byte(fieldMaps + y + ((uint8_t)(x >> 12) & 0x0f))) != 0x00) {
          break;
        }
        x -= t;
        y -= 0x10;
      }
      if (hm != 0x00) {
        hx = (uint8_t)(x >> 8);
        hy = y + 0x0f;
        hd = ((uint16_t)(positionY - hy) << 8) / pgm_read_byte(fieldCosDistances + 0xc0 - angle);
      }
    }
    
  // 270°
  } else if (angle == 0xc0) {
    uint8_t i = ((positionY & 0xf0) - 0x10) + (positionX >> 4);
    uint8_t m = 0x00;
    while ((m = pgm_read_byte(fieldMaps + i)) == 0x00) {
      i -= 0x10;
    }
    hx = positionX;
    hy = (i & 0xf0) + 0x0f;
    hd = positionY - hy;
    hm = m;

  // 270°〜360°
  } else {
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + 0x100 - angle);
      uint8_t x = (positionX >> 4) + 0x01;
      int32_t y = ((int32_t)positionY << 8) - (int32_t)(0x10 - (positionX & 0x0f)) * t;
      t *= 0x00000010;
      while (y >= 0x00000000) {
        if ((vm = pgm_read_byte(fieldMaps + ((uint8_t)(y >> 8) & 0xf0) + x)) != 0x00) {
          break;
        }
        x += 0x01;
        y -= t;
      }
      if (vm != 0x00) {
        vx = x << 4;
        vy = (uint8_t)(y >> 8);
        vd = ((uint16_t)(vx - positionX) << 8) / pgm_read_byte(fieldCosDistances + 0x100 - angle);
      }
    }
    {
      int32_t t = (int32_t)pgm_read_word(fieldTans + angle - 0xc0);
      int32_t x = ((int32_t)positionX << 8) + (int32_t)(0x01 + (positionY & 0x0f)) * t;
      uint8_t y = (positionY & 0xf0) - 0x10;
      t *= 0x00000010;
      while (x < 0x00010000) {
        if ((hm = pgm_read_byte(fieldMaps + y + ((uint8_t)(x >> 12) & 0x0f))) != 0x00) {
          break;
        }
        x += t;
        y -= 0x10;
      }
      if (hm != 0x00) {
        hx = (uint8_t)(x >> 8);
        hy = y + 0x0f;
        hd = ((uint16_t)(positionY - hy) << 8) / pgm_read_byte(fieldCosDistances + angle - 0xc0);
      }
    }
  }

  // 距離が近い方を選択
  if (vd <= hd) {
    *raycastX = vx;
    *raycastY = vy;
    *raycastDistance = vd;
    *raycastMap = vm;
    *raycastSlice = vy & 0x0f;
  } else {
    *raycastX = hx;
    *raycastY = hy;
    *raycastDistance = hd;
    *raycastMap = hm;
    *raycastSlice = hx & 0x0f;
  }
}


