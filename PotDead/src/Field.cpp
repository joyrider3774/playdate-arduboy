/*
 * フィールドを初期化する
 */
void FieldInitialize(void)
{
  // ビューの初期化
  for (int8_t i = 0x00; i < FIELD_VIEW; i++) {
    fieldViewDistances[i] = 0xff;
    fieldViewMaps[i] = 0x00;
    fieldViewSlices[i] = 0x00;
  }
  
  // 状態の設定
  fieldState = FIELD_STATE_NULL;
}

/*
 * フィールドを更新する
 */
void FieldUpdate(void)
{
  FieldCalcView();
}

